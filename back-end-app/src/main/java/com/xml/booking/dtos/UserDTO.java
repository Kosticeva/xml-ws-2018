package com.xml.booking.dtos;

public class UserDTO {
    String username;
    String password;
    String email;
    String firstName;
    String lastName;

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }
}
